package array2;

public class Arraywithforeachloop2 {

	public static void main(String[] args) {
		//WAP double array with for each loop and print array
		/*
		 * for(datatype variable:arrayname)
		 * {
		 * }
		 */
		
		double arr[] = {12.5,22.7,32.4,42.0,52.2};
		
		for (double x : arr) 
		{
			System.out.println(x);
			
		}
		

	}

}
