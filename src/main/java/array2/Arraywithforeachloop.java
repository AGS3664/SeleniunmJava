package array2;

public class Arraywithforeachloop {

	public static void main(String[] args) {
		//WAP for integer array with for each loop and print array
		/*
		 * for(datatype variable:arrayname)
		 * {
		 * }
		 */
		
		int arr[] = {12,22,32,42,52};
		
		for (int x : arr) 
		{
			System.out.println(x);
			
		}
		

	}

}
