package day5;



public class Switchdemo {

	public static void main(String[] args) {
		
		int x =3;
		
		switch (x) {
		case 1:
			System.out.println("Jan month");
			break;
		case 2:
			System.out.println("Feb month");
			break;
		case 3:
			System.out.println("March month");
			break;
		case 4:
			System.out.println("April month");
			break;
		default:
			break;
		}

	}

}
